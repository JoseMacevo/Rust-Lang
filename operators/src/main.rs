fn main () {
    // Operadores 
    // Numéricos Lógicos y Relacionales

    //Numéricos --> + - * / %
    let number_one: i32 = 10;
    let number_two: i32 = 100;
    let result: i32 = number_one + number_two;
    println!("The result of this operation is: {}", result);

    // Relacionales --> > < >= <= == !=
    let result: bool = result > 100;
    println!("The result of this operation is: {}", result);

    // Lógicos --> && (and) y || (or)
    let result: bool = 20 + 10 > 100 && true;
    println!("The result of this operation is: {}", result);

    

}   


