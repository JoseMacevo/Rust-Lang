pub fn run(){


  let number_one: i32 =10;
  let number_two: i32 = 200;

  // + - * / % -> mathematic operators.
  let result: i32 = number_one + number_two;
  println!("The result is: {} ", result);

  // Relational operators. > < >= <= !=
  let result: bool = result < 100;
  println!("The result is: {} ", result);

  // logic operators -> && ||

  let result: bool = 10 + 20 > 100 && true;
  println!("The result is: {} ", result);





}
